namespace PortableDocument
{
    /// <summary>
    /// Contains value of all special characters.
    /// </summary>
    public static class Character
    {
        /// <summary>
        /// LINE FEED (LF).
        /// </summary>
        public const byte LineFeed = 0x0A;

        /// <summary>
        /// CARRIAGE RETURN (CR).
        /// </summary>
        public const byte CarriageReturn = 0x0D;
    }
}
